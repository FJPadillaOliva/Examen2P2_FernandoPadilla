    private void btn_loginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_loginMouseClicked
        // TODO add your handling code here:
        String username = tf_username.getText();
        String password = tf_password.getText();
        boolean login = false;
        int tipoU = 0;
        for (Cliente t : clientes) {
            if (t.getUsername().equals(username) && t.getPassword().equals(password)) {
                login = true;
                tipoU = 1;
                cliente = t;
                break;
            }
        }
        for (Artista t : artistas) {
            if (t.getUsername().equals(username) && t.getPassword().equals(password)) {
                login = true;
                tipoU = 2;
                artista = t;
                break;
            }
        }
        if (login) {
            if (tipoU == 1) {
                JOptionPane.showMessageDialog(this, "Bienvenido " + username);
            } else {
                JOptionPane.showMessageDialog(this, "Bienvenido " + username);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Nombre de usuario o contrase√±a incorrecta");
        }
    }//GEN-LAST:event_btn_loginMouseClicked

